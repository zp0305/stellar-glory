// 辅助函数
export function getFormulaById(id: string): FormulaCard | undefined {
  return formulas.find(f => f.id === id)
}

export function searchFormulas(query: string): FormulaCard[] {
  const lowerQuery = query.toLowerCase()
  return formulas.filter(f =>
    f.name.toLowerCase().includes(lowerQuery) ||
    f.formula.toLowerCase().includes(lowerQuery) ||
    f.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    f.variables.some(v => v.name.toLowerCase().includes(lowerQuery))
  )
}